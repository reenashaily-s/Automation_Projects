/* data storage
JS supports 2 types : 1. Primitive,  2.  Non Primitive
*/


//Number
let num1 = 1000;
console.log(num1);
console.log("num1 is:" +num1);
console.log(typeof num1);

//bool
let isActive = true;
console.log(typeof isActive)

//string
let fName = 'Reena'
let LName = "Sompalli"
let WName = `Maddala`       //template string
console.log(typeof fName)
console.log(typeof LName)
console.log(typeof WName)

//undefined --> no value
let marks;
console.log("marks are:" +marks)
console.log(typeof marks)

//null
let address = null
console.log("What is address: " +address)
console.log(typeof address)

// object 
let user = {}
